# Получаем сторону квадрата от пользователя
side = float(input("Введите сторону квадрата: "))

# Вычисляем периметр квадрата
perimeter = 4 * side

# Вычисляем площадь квадрата
area = side ** 2

# Вычисляем диагональ квадрата
diagonal = side * (2 ** 0.5)

# Создаем кортеж из результатов
result = (perimeter, area, diagonal)

print("Периметр, площадь и диагональ квадрата:", result)
