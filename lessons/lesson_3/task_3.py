x = float(input("Введите сумму депозита: "))
y = int(input("Введите количество лет: "))
r = 0.10  # 10% в виде десятичной доли

# Вычисляем будущую стоимость депозита
future_value = x * (1 + r)**y

# Выводим результат
print(f"Через {y} лет на вашем счету будет: {future_value: }")


# Формула для расчета будущей стоимости депозита с процентами:
#
# FV = PV * (1 + r)^y
#
# Где:
#
# FV - будущая стоимость депозита (то, что мы хотим найти).
# PV - начальная сумма депозита (x в вашем случае).
# r - годовая процентная ставка в десятичных долях (10% будет 0.10).
# y - количество лет.